package negocio;

public class Autor {
	// Atributos da classe
	private String nome;
	
	// Metodos construtores
	public Autor() {
	}
	public Autor(String nome) {
		this.nome = nome;
	}
	
	// Metodos de acesso da classe
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	
}
